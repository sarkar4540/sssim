    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        int instructionQueueSize = Integer.parseInt(jTextField1.getText()),
                storageSize = Integer.parseInt(jTextField2.getText()),
                clockFrequency = Integer.parseInt(jTextField3.getText()),
                loadBufferSize = Integer.parseInt(jTextField4.getText()),
                storeBufferSize = Integer.parseInt(jTextField5.getText());
        HashMap<String, Integer> rtSizes = new HashMap<>();
        for (int i = 0; i < jTable1.getRowCount(); i++) {
            String opcode = jTable1.getValueAt(i, 0).toString();
            int rtSize = 0;
            try {
                rtSize = Integer.parseInt(jTable1.getValueAt(i, 2).toString());
            } catch (NumberFormatException e) {
                System.err.println("Invalid RT Size for " + opcode);
            }
            String type = jTable1.getValueAt(i, 1).toString();
            if (rtSize > 0) {
                if (type.equals("Arithmetic") || type.equals("Bitwise")) {
                    rtSizes.put(opcode, rtSize);
                } else {
                    System.err.println("Reservation stations are only for Arithmetic and Bitwise Opertions.");
                }
            }
        }
        Simulation sim = new Simulation(startAddress, instructionQueueSize, storageSize, clockFrequency, loadBufferSize, storeBufferSize, storage, rtSizes, addresses, jCheckBox1.isSelected(), this);
        sim.setVisible(true);
        setVisible(false);
    }//GEN-LAST:event_jButton2ActionPerformed

