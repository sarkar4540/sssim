    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            // TODO add your handling code here:
            jfc.showOpenDialog(this);
            File objdump = jfc.getSelectedFile();
            if (objdump != null) {
                jLabel1.setText(objdump.getName());
                Scanner sc = new Scanner(new FileReader(objdump));
                String storageText = "";
                storage = new HashMap<>();
                opcodes = new ArrayList<>();
                registers = new ArrayList<>();
                addresses = new ArrayList<>();
                Vector<Vector<String>> tabdata = new Vector<Vector<String>>();

                while (sc.hasNextLine()) {
                    var line = sc.nextLine().trim();
                    if (line.endsWith(":")) {
                        if (line.contains("main")) {
                            startAddress = line.split(" ")[0].replaceFirst("0+", "");
                        }
                    } else {
                        Instruction i = Instruction.fromDisassemblyString(line);
                        if (i.isValid()) {
                            storageText += i + "\n";
                            storage.put(i.getAddress(), i);
                            addresses.add(i.getAddress());
                            if (!i.hasValue()) {
                                if (!opcodes.contains(i.getOpcode())) {
                                    String op = i.getOpcode();
                                    opcodes.add(op);
                                    Vector<String> vec = new Vector<>();
                                    vec.add(op);
                                    vec.add(i.getTypeString());
                                    vec.add(i.getType() == Instruction.ARITHMETIC_OPS || i.getType() == Instruction.BITWISE_OPS ? "8" : "0");
                                    tabdata.add(vec);
                                }
                                String operands[] = i.getOperands();
                                for (String operand : operands) {
                                    if (operand.matches(".*\\([a-z]+[0-9]*\\)")) {
                                        operand = operand.split("[\\(\\)]")[1];
                                        if (!registers.contains(operand)) {
                                            registers.add(operand);
                                        }
                                    } else if (Register.fromName(operand).isValid()) {
                                        if (!registers.contains(operand)) {
                                            registers.add(operand);
                                        }
                                    }
                                }
                            }
                            minStorageSize = Integer.max(minStorageSize, Integer.parseInt(i.getAddress(), 16));

                        }
                    }
                }
                jTextArea1.setText(storageText);
                jLabel7.setText("Counts: " + opcodes.size() + " op(s), " + registers.size() + " reg(s), Start: " + startAddress);
                System.out.println("Opcodes:\n" + String.join("\n", opcodes) + "\n\nRegisters:\n" + String.join("\n", registers));
                jTextField2.setText("" + minStorageSize * 2);
                Vector<String> cols = new Vector<>();
                cols.add("Opcode");
                cols.add("Type");
                cols.add("RT Size");
                jTable1.setModel(new DefaultTableModel(tabdata, cols));
            }

        } catch (FileNotFoundException ex) {
            Logger.getLogger(SSSim.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_jButton1ActionPerformed
